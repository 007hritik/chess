import 'package:flutter/material.dart';

class ChessTheme {
  Color lightColor;
  Color darkColor;
  ChessTheme.Default() {
    lightColor = Colors.greenAccent[100];
    darkColor = Colors.green[400];
  }
  ChessTheme.GreemLemon() {
    lightColor = Colors.greenAccent[100];
    darkColor = Colors.green[400];
  }
}